#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*substr;

	if (!s || ft_strlen(s) <= start)
		return (ft_strdup(""));
	if (start + len > ft_strlen(s))
	{
		len = ft_strlen(s) - start;
	}
	substr = (char *)malloc((len + 1) * sizeof(char));
	if (!substr)
		return (NULL);
	ft_strlcpy(substr, s + start, len + 1);
	return (substr);
}
