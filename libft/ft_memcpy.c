#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char	*d_copy;
	unsigned char	*s_copy;

	d_copy = (unsigned char *)dst;
	s_copy = (unsigned char *)src;
	if (!dst && !src)
		return (NULL);
	while (n)
	{
		*d_copy++ = *s_copy++;
		n--;
	}
	return (dst);
}
